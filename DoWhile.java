import java.util.ArrayList; // Importamos a classe ArrayList para poder fazer uso do vetor e armazenar elementos.

public class DoWhile {
    /* Método principal da classe */
    public static void main(String[] args) {
        // Fluxo de repetição usando do-while
        int i = 5; // a variável i recebe o valor cinco (5)
        do {
            System.out.println(i); // imprima valor de i
            i++; // incremento da variável i.
        } while (i <3); // enquanto i for menor que 3

    } // fim do método main
} // fim da classe
